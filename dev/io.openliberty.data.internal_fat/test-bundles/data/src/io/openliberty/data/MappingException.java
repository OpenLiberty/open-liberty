package io.openliberty.data;

/**
 * Copied from jakarta.nosql.mapping.MappingException to investigate
 * how well the NoSQL template pattern works for relational database access.
 * This class directly inherits from RuntimeException rather than NoSQLException.
 */
public class MappingException extends RuntimeException {
    public MappingException() {
        super();
    }

    public MappingException(String message) {
        super(message);
    }

    public MappingException(String message, Throwable cause) {
        super(message, cause);
    }

    public MappingException(Throwable cause) {
        super(cause);
    }

    protected MappingException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
