package io.openliberty.org.apache.myfaces41.fat.rowStatePreserved;

public class Item {

    private String value;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString(){
        return value == null ? "" : value.toString();
    }
    
}
