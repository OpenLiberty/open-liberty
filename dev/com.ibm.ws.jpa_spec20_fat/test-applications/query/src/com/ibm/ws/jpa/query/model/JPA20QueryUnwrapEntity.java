package com.ibm.ws.jpa.query.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedNativeQueries;
import javax.persistence.NamedNativeQuery;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Version;

@Entity
@NamedQueries({ @NamedQuery(name = "UnwrapQueryNamed", query = "SELECT e from JPA20QueryUnwrapEntity e") })
@NamedNativeQueries({ @NamedNativeQuery(name = "UnwrapQueryNative", query = "SELECT e from JPA20QueryUnwrapEntity e") })
public class JPA20QueryUnwrapEntity {
    @Id
    private int id;

    private String strData;

    @Version
    private long version;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStrData() {
        return strData;
    }

    public void setStrData(String strData) {
        this.strData = strData;
    }

    public long getVersion() {
        return version;
    }

}
