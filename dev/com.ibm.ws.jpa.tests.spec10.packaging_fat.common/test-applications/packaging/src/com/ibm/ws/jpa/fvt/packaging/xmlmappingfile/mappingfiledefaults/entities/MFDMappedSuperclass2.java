package com.ibm.ws.jpa.fvt.packaging.xmlmappingfile.mappingfiledefaults.entities;

/**
 * Mapped superclass class, declared with a non qualified classname in the XML ORM file, relying on the package mapping
 * default setting for the JPA implementation to find it.
 *
 */
public abstract class MFDMappedSuperclass2 implements MappingFileEntity {
    private int id;
    private String name;

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "MFDMappedSuperclass2 [id=" + id + ", name=" + name + "]";
    }
}
