package com.ibm.ws.jpa.fvt.packaging.xmlmappingfile.mappingfiledefaults.entities;

/**
 * Entity extending the MFDMappedSuperclass2 mapped superclass.
 *
 *
 */
public class MFDMSC2Ent extends MFDMappedSuperclass2 {
    private String street;
    private String city;
    private String state;
    private String zip;

    @Override
    public String getCity() {
        return city;
    }

    @Override
    public void setCity(String city) {
        this.city = city;
    }

    @Override
    public String getState() {
        return state;
    }

    @Override
    public void setState(String state) {
        this.state = state;
    }

    @Override
    public String getStreet() {
        return street;
    }

    @Override
    public void setStreet(String street) {
        this.street = street;
    }

    @Override
    public String getZip() {
        return zip;
    }

    @Override
    public void setZip(String zip) {
        this.zip = zip;
    }

    @Override
    public String toString() {
        return "MFDMSC2Ent [street=" + street + ", city=" + city + ", state=" + state + ", zip=" + zip + "]";
    }
}