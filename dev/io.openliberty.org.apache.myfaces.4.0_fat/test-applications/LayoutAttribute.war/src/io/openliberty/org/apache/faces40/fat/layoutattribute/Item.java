package io.openliberty.org.apache.faces40.fat.layoutattribute;

/**
 * POJO that represents an item in a shop
 */
public class Item {

    long id;
    String name;
    int stock;
    double price;

    /**
     * @param id the item id
     * @param name the item name
     * @param stock the stock of item
     * @param price the price of item
     */
    public Item(long id, String name, int stock, double price) {
        super();
        this.id = id;
        this.name = name;
        this.stock = stock;
        this.price = price;
    }

    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the stock
     */
    public int getStock() {
        return stock;
    }

    /**
     * @param stock the stock to set
     */
    public void setStock(int stock) {
        this.stock = stock;
    }

    /**
     * @return the price
     */
    public double getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(double price) {
        this.price = price;
    }

}
