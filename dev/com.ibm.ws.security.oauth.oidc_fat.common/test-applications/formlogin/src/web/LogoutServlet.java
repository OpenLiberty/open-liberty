package web;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

public class LogoutServlet extends HttpServlet {
    private static final long serialVersionUID = 1L;

    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        System.out.println("Test application class LogoutServlet is logging out");
        req.logout();
        System.out.println("Test application class LogoutServlet logged out");

        String redirectTo = req.getParameter("post_logout_redirect_uri");
        System.out.println("Test application class LogoutServlet redirecting to: " + redirectTo);
        resp.sendRedirect(redirectTo);
    }

}
