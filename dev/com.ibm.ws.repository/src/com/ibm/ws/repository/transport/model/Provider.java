package com.ibm.ws.repository.transport.model;

public class Provider extends AbstractJSON {

    private String name;
    // Use a string here, as URL's are bad (equals method, see findbugs), and URI's are a pain to work with.
    private String url;

    public Provider() {
    }

    /**
     * Copy constructor
     *
     * @param other the object to copy
     */
    public Provider(Provider other) {
        super();
        this.name = other.name;
        this.url = other.url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((url == null) ? 0 : url.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Provider other = (Provider) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (url == null) {
            if (other.url != null)
                return false;
        } else if (!url.equals(other.url))
            return false;
        return true;
    }
}
